//PROGRAM WHICH DESCRIBES THE VARIOUS FUCTIONS OF STRING IN JAVA.
public class Fuctions_Of_String_3{
    public static void main(String[] args) {
        
        String Name = "MARTIN GARRIX";

        //12.INT INDEXOF(CHAR CH): - 
        System.out.println(Name.indexOf("X"));

        //13.STRING TOLOWERCASE(): - 
        System.out.println(Name.toLowerCase());

        //14.STRING TOUPPERCASE: - 
        System.out.println(Name.toUpperCase());

        //15.STRING TRIM: - 
        String Name2 = "   HARDWELL    ";
        System.out.println(Name2);
        System.out.println(Name2.trim());

    }
}