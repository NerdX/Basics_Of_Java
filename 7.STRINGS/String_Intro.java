// PROGRAM FOR INTRODUCTION OF STRINGS IN JAVA.
public class String_Intro{
    public static void main(String[] args) {
        
        //CREATION OF STRING BY LITERALS: - 
        String Name = "MARTIN GARRIX";
        System.out.println("NAME = "+" "+Name);

        //BY CREATING AN OBJECT OF STRING CLASS: - 
        String Name1 = new String("ALAN WALKER");
        System.out.println("NAME1 = "+" "+Name1);
    }
}