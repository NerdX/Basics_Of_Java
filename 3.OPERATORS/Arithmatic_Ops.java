//PROGRAM FOR ARITHMATIC-OPERATORS IN JAVA.
public class Arithmatic_Ops {
    public static void main(String[] args) {
        int a=20;
        int b=10;

        int c = (a+b);
        int d = (a-b);
        int x = (a/b);
        int y = (a*b);
        int m = ++a;
        int n = ++b;
        int modulo = (a % b);
        System.out.println("THE ADDIDTION = "+c);
        System.out.println("THE SUBTRACTION = "+d);
        System.out.println("THE DIVISION = "+x);
        System.out.println("THE MULTIPLICATION = "+y);
        System.out.println("THE INCREMENT OF A = "+m);
        System.out.println("THE INCREMENT OF B = "+n);
        System.out.println("THE MODULO = "+modulo);
    }
}